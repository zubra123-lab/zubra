function getLang() {
  const l = localStorage.getItem(LANG_KEY);
  return l && I18N[l] ? l : "it";
}
function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang && I18N[lang] ? lang : "it");
  applyLang();
}
// Traduzione: t("key") oppure t("key", arg) per le voci-funzione.
function t(key, ...args) {
  const dict = I18N[getLang()] || I18N.it;
  // fallback: lingua scelta → inglese → italiano → la chiave stessa
  const v = (key in dict) ? dict[key] : (I18N.en[key] ?? I18N.it[key] ?? key);
  return typeof v === "function" ? v(...args) : v;
}

// Applica le traduzioni a tutti gli elementi statici marcati con data-i18n.
function applyLang() {
  const lang = getLang();
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });
  // selettore lingua (menù a tendina) + eventuali bottoni
  const sel = document.getElementById("langSelect");
  if (sel) sel.value = lang;
  document.querySelectorAll("[data-lang]").forEach((b) => {
    b.classList.toggle("is-active", b.getAttribute("data-lang") === lang);
  });
  // aggiorna le parti dinamiche se le funzioni esistono
  if (typeof refreshDynamicTexts === "function") refreshDynamicTexts();
}

document.addEventListener("DOMContentLoaded", () => {
  const sel = document.getElementById("langSelect");
  if (sel) sel.addEventListener("change", () => setLang(sel.value));
  document.querySelectorAll("[data-lang]").forEach((b) => {
    b.addEventListener("click", () => setLang(b.getAttribute("data-lang")));
  });
  applyLang();
});
